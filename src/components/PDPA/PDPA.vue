                  <li>	- ข้อมูลที่แสดงตัวตนอย่างถูกต้องตามกฎหมาย</li>
                  <li>	- ข้อมูลการเข้าใช้งานในระบบของบริษัท</li>
                  <li>	- ข้อมูลตำแหน่ง</li>
                  <li>	- ข้อมูลส่วนบุคคลที่ผู้ใช้บริการอาจมีการให้แก่บริษัทผ่านทางระบบ</li>
                </ul>
              </p>
              <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                <span><b>3.4</b></span
                >&nbsp;&nbsp;&nbsp;&nbsp;การเก็บรักษาข้อมูลส่วนบุคคลและการใช้ข้อมูลส่วนบุคคล
        บริษัทปฏิบัติตามนโยบายความเป็นส่วนตัวอย่างมีมาตรฐานและได้นำมาตรการด้านความปลอดภัยมาใช้ด้วยความพยายามอย่างดีที่สุด เพื่อให้แน่ใจว่าข้อมูลส่วนตัวของผู้ใช้บริการมีความปลอดภัย ข้อมูลส่วนตัวของผู้ใช้บริการจะถูกเก็บไว้ในเครือข่ายที่มีความปลอดภัยและสามารถเข้าถึงได้โดยบุคลากรภายในองค์กรบางส่วนที่มีจำนวนจำกัดเท่านั้น ผู้ใช้บริการมีหน้าที่รับผิดชอบในการรักษาความลับของข้อมูลใด ๆ ที่บริษัทส่งถึง ผู้ใช้บริการ และในการปฏิบัติตามขั้นตอนด้านความปลอดภัยต่าง ๆ ที่เราได้แจ้งให้ผู้ใช้บริการทราบ โดยเฉพาะรหัสผ่านที่ทำให้ผู้ใช้บริการสามารถเข้าระบบของบริษัท เราขอให้ผู้ใช้บริการไม่เปิดเผยรหัสผ่านนี้แก่ผู้ใดก็ตาม มิฉะนั้นเราไม่สามารถรับประกันความปลอดภัยของข้อมูลส่วนบุคคลของผู้ใช้บริการได้อย่างสมบูรณ์

              </p>
              <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                <span><b>3.5</b></span
                >&nbsp;&nbsp;&nbsp;&nbsp;การเปิดเผยข้อมูลส่วนบุคคล
        บริษัทจะไม่เปิดเผยข้อมูลดังกล่าวต่อบุคคลใดโดยปราศจากอนุญาตจากผู้ใช้บริการ อย่างไรก็ตามเพื่อประโยชน์สูงสุดของผู้ใช้บริการและการให้บริการของบริษัท ทั้งนี้ผู้ใช้บริการจะต้องรับทราบและยินยอมว่าบริษัทอาจมีการเปิดเผยข้อมูลส่วนบุคคลของผู้ใช้บริการสู่บุคคลที่สามหรือเพื่อนำข้อมูลส่วนบุคคลของท่านไปใช้ประโยชน์ในการเพิ่มประสิทธิภาพการให้บริการมากยิ่งขึ้น หรือเพื่อปรับปรุงและพัฒนารูปแบบการให้บริการและการเข้าถึงเนื้อหาต่างๆรวมถึงเพื่อประโยชน์ในการดำเนินการของบริษัท โดยในการเปิดเผยข้อมูลส่วนตัวของผู้ใช้บริการดังกล่าวนี้ บริษัทจะดำเนินการให้บุคคลเหล่านั้นเก็บรักษาข้อมูลส่วนบุคคลของผู้ใช้บริการไว้เป็นความลับอย่างถึงที่สุด และไม่นำไปใช้เพื่อวัตถุประสงค์อื่นนอกเหนือจากขอบเขตที่บริษัทกำหนดไว้

              </p>
              <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                <span><b>3.6</b></span
                >&nbsp;&nbsp;&nbsp;&nbsp;การตลาดหรือการส่งเสริมการขาย
        บริษัทอาจนำข้อมูลส่วนบุคคลของผู้ใช้บริการไปใช้เพื่อวิเคราะห์ความต้องการของผู้ใช้บริการใหม่ในอนาคตของทางบริษัทและแนวโน้มของตลาด รวมทั้งเพื่อให้ได้รับข้อมูลเชิงลึก ซึ่งทางบริษัทอาจนำไปใช้เพื่อปรับเปลี่ยน ปรับปรุง แก้ไข และพัฒนาระบบ การดำเนินการนี้อาจทำให้ทางบริษัทต้องนำข้อมูลส่วนบุคคลที่บริษัทเก็บรักษาไว้เกี่ยวกับการที่ผู้ใช้บริการได้ใช้บริการต่างๆ ของระบบ ไปรวมกับข้อมูลที่ทางบริษัทได้เก็บรวบรวมเกี่ยวกับการใช้งานในระบบของผู้ใช้บริการ และทางบริษัทจะนำไปเก็บรวบรวมเกี่ยวกับผู้ใช้บริการท่านอื่น ๆ เพื่อให้ได้มาซึ่งข้อมูลเชิงลึกและทราบแนวโน้มของตลาด โดยทางบริษัทอาจให้ข้อมูลเชิงลึกเหล่านี้แก่พันธมิตรที่เป็นบุคคลภายในองค์กรเพื่อนำมาซึ่งกระบวนการในการวิเคราะห์เพื่อจุดประสงค์ในการส่งเสริมโปรโมชั่น ทั้งนี้เพื่อส่งเสริมกิจกรรมทางการตลาดดังกล่าวนี้เราจะแจ้งบอกกล่าวสื่อสารในวิธีการและรูปแบบต่างๆ เกี่ยวกับการทำการตลาด และ ข้อมูลและสื่อเกี่ยวกับกิจกรรมส่งเสริมการตลาดที่เกี่ยวข้องกับระบบและการบริการ อาจจำหน่าย ทำการตลาด หรือส่งเสริมการขาย ซึ่งผู้ใช้บริการสามารถยกเลิกการรับข้อมูลโฆษณาทางการตลาดได้ตลอดเวลาโดยการแจ้งมายังบริษัท ที่จัดให้มีในสื่อการตลอดอิเล็กทรอนิกส์ทั้งในแพลตฟอร์ม Facebook และแพลตฟอร์ม Line Official Account หรืออื่นๆตามบริษัทเห็นสมควรโดยบริษัทอาจใช้ข้อมูลสำหรับติดต่อของผู้ใช้บริการ หรือสื่อการตลาดจากเรา หรือบริษัทที่เกี่ยวข้อง กฎหมายคุ้มครองข้อมูลส่วนบุคคล


              </p>
              <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                <span><b>3.7</b></span
                >&nbsp;&nbsp;&nbsp;&nbsp;        จัดเก็บข้อมูลส่วนบุคคลของลูกค้าและจำกัดสิทธิ์การเข้าถึงข้อมูลเฉพาะผู้ที่ได้รับอนุญาตเท่านั้น โดยทางบริษัท ได้นำมาตรการด้านความปลอดภัยที่เหมาะสมกับความเสี่ยงที่เกิดขึ้น ตามการพัฒนาทางเทคโนโลยี และ จะมีเพียงเฉพาะฝ่ายพัฒนาระบบของทางบริษัทที่จะสามารถเข้าถึงข้อมูลส่วนตัวของผู้ใช้บริการได้เท่านั้น หากผู้ใช้มีความประสงค์ให้ทางบริษัทลบข้อมูลส่วนตัวของผู้ใช้บริการตามนโยบายการรักษาข้อมูลส่วนบุคคลของผู้ใช้บริการออกนั้น สามารถแจ้งทางบริษัทได้ทันที
              </p>
              
              <b>4. ข้อจำกัดความรับผิด</b><br />
              <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                <span><b>4.1</b></span
                >&nbsp;&nbsp;&nbsp;&nbsp;บริษัทจะรับผิดต่อความเสียหายที่มีสาเหตุมาจากระบบเท่านั้น ความผิดพลาดที่เกิดจากสัญญาณ, ซอฟต์แวร์ ระบบ หรือ/และอุปกรณ์ของผู้ใช้บริการและผู้ให้บริการบุคคลที่สามที่ไม่ได้อยู่ในความควบคุมของบริษัท บริษัทไม่ต้องรับผิดชอบต่อความเสียหายดังกล่าว โดยเฉพาะ
              </p>
              <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                <span><b>4.1.1</b></span
                >&nbsp;&nbsp;&nbsp;&nbsp;บริษัทจะไม่รับผิดชอบต่อการสูญหายหรือความเสียหายใด ๆ ของพัสดุ เนื่องจากบริษัทไม่ได้มีหน้าที่และความรับผิดชอบในการดูแลและจัดส่งพัสดุของผู้ใช้บริการ บริษัทเป็นเพียงผู้ให้บริการระบบจัดการร้านค้าออนไลน์ และให้อัตราค่าบริการส่งพัสดุกับผู้ใช้บริการในอัตราพิเศษเท่านั้น
              </p>
              <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                <span><b>4.1.2</b></span
                >&nbsp;&nbsp;&nbsp;&nbsp;บริษัทจะไม่รับผิดชอบต่อค่าบริการส่งพัสดุและยอดชำระเงินปลายทาง (COD) ที่ทางผู้ให้บริการขนส่งทำการเรียกเก็บกับผู้ใช้บริการ เว้นแต่จะเป็นไปตามดุลยพินิจของทางบริษัท
              </p>
              <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                <span><b>4.1.3</b></span
                >&nbsp;&nbsp;&nbsp;&nbsp;บริษัทจะไม่รับผิดชอบต่อการกระทำความผิดตามกฎหมายใด ๆ ของผู้ใช้บริการ เนื่องจากบริษัทเป็นเพียงผู้ให้บริการระบบจัดการร้านค้าออนไลน์ ไม่ได้มีหน้าที่และความรับผิดชอบในการจัดการร้านค้าของผู้ใช้บริการ และหากมีหน่วยงานแจ้งเรื่องร้องเรียนของผู้ใช้บริการมายังบริษัท บริษัทจะแจ้งไปยังผู้ใช้บริการและขอความร่วมมือในการดำเนินการตรวจสอบตามขั้นตอนอีกครั้ง
              </p>
              <b>5. พัสดุที่ไม่สามารถส่งได้</b><br />
              <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                       ผู้ใช้บริการยอมรับและตกลงในเงื่อนไขการส่งพัสดุของผู้ให้บริการขนส่งที่ไม่สามารถจัดส่งได้ตามกฎหมายและกฎระเบียบต่าง ๆ ที่เกี่ยวข้องของแต่ละผู้ให้บริการขนส่ง และ/หรือ เป็นพัสดุตามรายการดังต่อไปนี้       
              </p>
               
              <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                <span><b>5.1</b></span
                >&nbsp;&nbsp;&nbsp;&nbsp;พัสดุที่ผู้ให้บริการขนส่งเห็นว่าไม่สมควรในการจัดส่ง หรือ พัสดุที่ไม่ได้มาตรฐานตามที่ผู้ให้บริการขนส่งกำหนด
              </p>
              <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                <span><b>5.2</b></span
                >&nbsp;&nbsp;&nbsp;&nbsp;ข้อสองอาวุธทุกชนิด เช่น ปืน กระสุน ระเบิด วัตถุมีคมที่ปราศจากสิ่งหุ้มห่อ ยุทธภัณฑ์ เป็นต้นง
              </p>
              <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                <span><b>5.3</b></span
                >&nbsp;&nbsp;&nbsp;&nbsp;สื่อสิ่งพิมพ์ที่ผิดกฎหมาย ลามกอนาจารหรือเป็นอันตรายต่อความมั่นคงของชาติและสังคม เช่น สื่อโฆษณาชวนเชื่อต่าง ๆ และหมายความรวมถึงวัตถุลามกอนาจาร เป็นต้น
              </p>
              <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                <span><b>5.4</b></span
                >&nbsp;&nbsp;&nbsp;&nbsp;วัตถุไวไฟและวัตถุระเบิดทุกชนิด เช่น วัสดุติดไฟ ดินปืน ประทัด ของเหลวไวไฟ แก็ส ของแข็ง น้ำมันเครื่อง น้ำมันแลคเกอร์ แบตเตอรี่ ไฟแช็ก ถังแก็ส ฟอสฟอรัส กำมะถัน ไม้ขีดไฟ กระป๋องฉีดพ่นหรือกระป๋องสเปรย์ เป็นต้น
              </p>
              <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                <span><b>5.5</b></span
                >&nbsp;&nbsp;&nbsp;&nbsp;สารชีวเคมีเช่นกรดซัลฟูริก กรดไฮโดรคลอริก กรดไนตริก ตัวทำละลายอินทรีย์ สารกำจัดศัตรูพืช ไฮโดรเจนเปอร์ออกไซด์ สารเคมีอันตราย เป็นต้น
              </p>
              <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                <span><b>5.6</b></span
                >&nbsp;&nbsp;&nbsp;&nbsp;ภาชนะที่บรรจุส่วนประกอบของสารกัมมันตรังสี เช่นยูเรเนียมโคบอลต์ เรเดียม พลูโทเนียม เป็นต้น
              </p>
              <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                <span><b>5.7</b></span
                >&nbsp;&nbsp;&nbsp;&nbsp;สารเคมีที่มีพิษสูงเช่น แทลเลียม ไซยาไนด์ สารหนู เป็นต้น
              </p>
              <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                <span><b>5.8</b></span
                >&nbsp;&nbsp;&nbsp;&nbsp;ยาเสพติดหรือสิ่งเสพติดที่ผิดกฎหมายทุกชนิด ตามพระราชบัญญัติสารเสพติดให้โทษ เช่น ฝิ่น (รวมถึงต้นฝิ่น ดอกฝิ่นและใบฝิ่น) มอร์ฟีน โคเคน เฮโรอีน ยาบ้า ยาอี สารอีเฟดรีน สารแอมเฟตามีน เป็นต้น
              </p>
              <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                <span><b>5.9</b></span
                >&nbsp;&nbsp;&nbsp;&nbsp;ผลิตภัณฑ์ชีวเคมีประเภทต่างๆและวัตถุที่ก่อให้เกิดการแพร่ระบาดและติดเชื้อ เช่น สารแอนแทรกซ์ เชื้อโรคอันตราย รวมถึงของเสียทางการแพทย์ เป็นต้น
              </p>
              <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                <span><b>5.10</b></span
                >&nbsp;&nbsp;&nbsp;&nbsp;สัตว์ สิ่งมีชีวิต ร่างกายมนุษย์ เช่นเนื้อเยื่อ อวัยวะมนุษย์ เป็นต้น
              </p>
              <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                <span><b>5.11</b></span
                >&nbsp;&nbsp;&nbsp;&nbsp;สินค้าที่ผิดกฎหมายและไม่เป็นไปตามข้อบังคับทางกฎหมายของประเทศเช่น เอกสารลับทางราชการ สินค้าลักลอบนำเข้าจากต่างประเทศ เช่น สินค้าผิดกฎหมาย ลักลอบหนีภาษี หลีกเลี่ยงภาษี สำแดงเท็จ รวมถึงสินค้าต้องห้ามและต้องจำกัด
              </p>
              <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                <span><b>5.12</b></span
                >&nbsp;&nbsp;&nbsp;&nbsp;เอกสารทั้งหมด รวมทั้งเอกสารทั่วไปและเอกสารต่าง ๆ ที่ทางราชการออกให้ เช่น บัตรประจำตัวประชาชน ใบขับขี่ หนังสือเดินทาง สูติบัตร เอกสารสัญญา พินัยกรรม และฉโนดที่ดิน เป็นต้น
              </p>
              <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                <span><b>5.13</b></span
                >&nbsp;&nbsp;&nbsp;&nbsp;เอกสารที่เกี่ยวกับสถาบันการเงิน เช่น เงินตราทุกสกุล (เงินสด) เช็ค บัตรเครดิต บัตรเดบิตและใบจำนำ สลากกินแบ่งรัฐบาล สลากออมสิน เป็นต้น
              </p>
              <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                <span><b>5.14</b></span
                >&nbsp;&nbsp;&nbsp;&nbsp;สินค้าที่ไม่สามารถประเมินค่าได้ สิ่งของที่มีมูลค่าทางจิตใจ หรือสินค้าที่ไม่สามารถทดแทนได้ เช่น ไข่มุก อัญมณี วัสดุโลหะล้ำค่า เครื่องสังฆภัณฑ์ พระพุทธรูป งานศิลปะ ภาพวาดและโบราณวัตถุ ทองคำแท่ง ทองรูปพรรณ วัตถุมงคล-พระเครื่อง เครื่องประดับเงิน เป็นต้น รวมถึงสิ่งของปลอมแปลงหรือลอกเลียนแบบโดยละเมิดลิขสิทธิ์
              </p>
              <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                <span><b>5.15</b></span
                >&nbsp;&nbsp;&nbsp;&nbsp;อุปกรณ์การเรียนทางการแพทย์ รวมถึงตัวอย่างสัตว์และพืช เช่น ตัวอย่างวิจัย ตัวอย่างเลือด ตัวอย่างปัสสาวะ สารคัดหลั่งต่าง ๆ เป็นต้น
              </p>
              <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                <span><b>5.16</b></span
                >&nbsp;&nbsp;&nbsp;&nbsp;จดหมาย และไปรษณียบัตร ทุกชนิด
              </p>
              <b>6. ข้อจำกัดความรับผิด</b><br />
              <p>
                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                ผู้ใช้บริการจะต้องไม่กระทำการใด ๆ
                ทั้งทางตรงและทางอ้อมในการเสนอขายบัญชีหรือโอนสิทธิในการใช้งานระบบ
                ไม่ว่าจะทั้งหมดหรือบางส่วนไปยังบุคคลภายนอก
                ไม่ว่าบุคคลดังกล่าวจะเป็นลูกค้าของผู้ให้บริการใดก็ตาม
                เว้นแต่จะได้รับความยินยอมเป็นลายลักษณ์อักษรจากบริษัท
              </p>
