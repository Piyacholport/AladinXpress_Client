                โดยยอดเงินที่ระงับไว้ดังกล่าวใช้เพื่อสำรองจ่ายในการแก้ไขเยียวยากับผู้เสียหายที่ได้รับผลกระทบจากการส่งพัสดุไม่ตรงคำสั่งซื้อและ/หรือได้รับพัสดุทั้งที่ไม่ได้มีการสั่งซื้อ
